#include<stdio.h>

int ft_strncmp(char *s1, char *s2, int n)
{
    int i = 0;

    while(i < n)
    {
        if(s1[i] != s2[i] || s1[i] == '\0' || s2[i] == '\0')
        { 
            return s1[i] - s2[i];   
        }
        i++;
    }
    return 0;
}